# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1') + 1) # Coersão
print(float(1) + 1)
print(int(True) + 1) 
# Estamos convertendo um tipo de dado para outro, 
# porque o Python é de tipagem forte, isso significa
# que o mesmo não reconhece a conversão de tipo sozinho.